.set noreorder
.set noat

.align 2
.globl expand_blend_mips

# a0 = screen_src_ptr (u32*)
# a1 = screen_dest_ptr (u16*)
# a2 = start
# a3 = end
expand_blend_mips:
  subu   $t0, $a3, $a2
  blez   $t0, blend_return
  nop

  sll    $t1, $a2, 2
  addu   $a0, $a0, $t1
  sll    $t1, $a2, 1
  addu   $a1, $a1, $t1

  lui    $t2, %hi(io_registers)
  addiu  $t2, $t2, %lo(io_registers)
  lhu    $t3, 0x52($t2)          # REG_BLDALPHA

  andi   $t4, $t3, 0x1F          # blend_a
  srl    $t5, $t3, 8
  andi   $t5, $t5, 0x1F          # blend_b

  slti   $t6, $t4, 17
  bne    $t6, $zero, 1f
  nop
  li     $t4, 16
1:
  slti   $t6, $t5, 17
  bne    $t6, $zero, 2f
  nop
  li     $t5, 16
2:
  addu   $t6, $t4, $t5
  slti   $t7, $t6, 17            # 1 if no saturation path

  lui    $t8, %hi(palette_ram)
  addiu  $t8, $t8, %lo(palette_ram)
  li     $t9, 0x04000200         # combine mask
  li     $v0, 0x03E07C1F         # clamp mask (matches C path)
  li     $v1, 0x04008020         # saturation bits mask (matches C path)

blend_loop:
  lw     $t1, 0($a0)
  addiu  $a0, $a0, 4

  and    $t2, $t1, $t9
  bne    $t2, $t9, blend_no_blend
  nop

  andi   $t2, $t1, 0x1FF
  sll    $t2, $t2, 1
  addu   $t2, $t2, $t8
  lhu    $t2, 0($t2)             # pixel_top

  srl    $t3, $t1, 16
  andi   $t3, $t3, 0x1FF
  sll    $t3, $t3, 1
  addu   $t3, $t3, $t8
  lhu    $t3, 0($t3)             # pixel_bottom

  sll    $t6, $t2, 16
  or     $t2, $t2, $t6
  and    $t2, $t2, $v0           # top dilated

  sll    $t6, $t3, 16
  or     $t3, $t3, $t6
  and    $t3, $t3, $v0           # bottom dilated

  multu  $t3, $t5
  mflo   $t3
  multu  $t2, $t4
  mflo   $t2
  addu   $t2, $t2, $t3

  bne    $t7, $zero, blend_no_saturate
  nop

  srl    $t2, $t2, 4
  and    $t3, $t2, $v1
  beq    $t3, $zero, blend_no_saturate
  nop
  andi   $t6, $t3, 0x0020
  beq    $t6, $zero, 3f
  nop
  ori    $t2, $t2, 0x001F
3:
  li     $t6, 0x00008000
  and    $t6, $t3, $t6
  beq    $t6, $zero, 4f
  nop
  li     $t6, 0x00007C00
  or     $t2, $t2, $t6
4:
  lui    $t6, 0x0400             # 0x04000000
  and    $t6, $t3, $t6
  beq    $t6, $zero, blend_no_saturate
  nop
  lui    $t6, 0x03E0
  or     $t2, $t2, $t6

blend_no_saturate:
  and    $t2, $t2, $v0
  srl    $t3, $t2, 16
  or     $t2, $t2, $t3
  sh     $t2, 0($a1)
  addiu  $a1, $a1, 2
  b      blend_next
  nop

blend_no_blend:
  andi   $t2, $t1, 0x1FF
  sll    $t2, $t2, 1
  addu   $t2, $t2, $t8
  lhu    $t2, 0($t2)
  sh     $t2, 0($a1)
  addiu  $a1, $a1, 2

blend_next:
  addiu  $t0, $t0, -1
  bgtz   $t0, blend_loop
  nop

blend_return:
  jr     $ra
  nop
